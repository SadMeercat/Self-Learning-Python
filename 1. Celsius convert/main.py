# –ü–æ—Å–ª–µ –≤–≤–æ–¥–∞ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä—ã –ø—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–ø–æ–ª–Ω—è—Ç—å –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏—é –∏ –≤—ã–≤–æ–¥–∏—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç.
# –§–æ—Ä–º—É–ª—ã –¥–ª—è –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–∏:
# –ò–∑ –¶–µ–ª—å—Å–∏—è –≤ –§–∞—Ä–µ–Ω–≥–µ–π—Ç: 
# ùêπ=9/5√óùê∂+32
# –ò–∑ –§–∞—Ä–µ–Ω–≥–µ–π—Ç–∞ –≤ –¶–µ–ª—å—Å–∏–π: 
# ùê∂=5/9√ó(ùêπ‚àí32)

def celsius_to_farengeit(celsius):
    return 9/5 * celsius + 32

def farengeit_to_celsius(farengeit):
    return 5/9 * (farengeit - 32)

